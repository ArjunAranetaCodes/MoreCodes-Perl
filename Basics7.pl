#If, ElseIf, and Else Structure

$num1 = 1;
$num2 = 2;

#if statement
if($num1>$num2){
 print "First number is higher!\n";
}

#if-else statement
if($num1==$num2){
 print "They are equal!\n";
}else{
 print "They are not equal!\n";
}

#if-else if statement
if($num1>$num2){
 print "First number is greater!\n";
}elsif($num1<$num2){
 print "Second number is greater!\n";
}




